package medi.nf;

/**
 *
 * @author victor
 */
public class IdDM {
    private int annee;
    private int mois;
    private int compteur;
    
    public IdDM(int annee, int mois, int compteur){
        this.annee = annee;
        this.compteur = compteur;
        this.mois = mois;
    }

    public int getAnnee() {
        return annee;
    }

    public void setAnnee(int annee) {
        this.annee = annee;
    }

    public int getMois() {
        return mois;
    }

    public void setMois(int mois) {
        this.mois = mois;
    }

    public int getCompteur() {
        return compteur;
    }

    public void setCompteur(int compteur) {
        this.compteur = compteur;
    }
    
    
    
}
